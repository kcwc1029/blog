#include <stdio.h>
#include <stdlib.h>
void strcopy(char src[], char dst[], int n)
{
    int i;
    for ( i = 0; i < n; i++ )
        dst[i] = src[i];  /* 將 src 陣列內容放至 dst */ 
}        
int main()
{
    char str1[] = {'D','e','e','p','M','i','n','d','\0'};
    char str2[10];
    int  len;

	len = sizeof(str1);   /* 因為是字元陣列所以直接就是元素數量 */ 
    strcopy(str1,str2,len);
    printf("來源字元陣列 : %s\n",str1);
    printf("目的字元陣列 : %s\n",str2);
    system("pause");
    return 0;
}












































































































































































































