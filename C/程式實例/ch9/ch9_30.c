#include <stdio.h>
#include <stdlib.h>
void strcopy(char src[], char dst[])
{
    int i, len;
    len = sizeof(src);
    for ( i = 0; i < len; i++ )
        dst[i] = src[i];  /* 將 src 字串內容放至 dst */ 
}        
int main()
{
    char str1[] = "DeepMind";
    char str2[10];

    strcopy(str1,str2);
    printf("來源字串 : %s\n",str1);
    printf("目的字串 : %s\n",str2);
    system("pause");
    return 0;
}












































































































































































































